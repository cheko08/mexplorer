<p><strong>Visa</strong></p>
<p>Pour les ressortissants de l'Union européenne, canadiens et suisses : un passeport, valable encore six mois après le retour, suffit pour entrer au Mexique. Une carte de tourisme est délivrée gratuitement à l'arrivée. </p>


<p><strong>Electricité</strong></p>
<p>110 V, 60 Hz, prise de type A ou B selon la classification ANF.</p>

<p><strong>Argent</strong> </p>
<p>Monnaie : peso mexicain $MXN</p>
<p>Taux de conversion : 1 euro=17,50 pesos ; 1USD=12.50 pesos (taux de conversion approximatifs sur l’année 2014)</p>
<p>Pourboire : au Mexique le pourboire fait partie intégrante du mode de consommation, ne l’oubliez pas les gens qui vous fournissent le service comptent dessus ! </p>
<p>Pour les services, il faut compter 10% de la consommation, 15% si vous êtes très content du service. </p>
<p>Pour les  guides et les chauffeurs il faut compter un euro par jour de service et par personnes, avec un petit plus si vous êtes très content du service. </p>
<p><strong>Précautions élémentaires</strong></p>
<p>-    Pensez à prendre du répulsif anti moustique (type cinq sur cinq, vendu en pharmacie). En effet, le Mexique est un pays tropical et de ce fait la population de moustiques y est importante. </p>
<p>-    Couvrez-vous, le soleil n’est pas votre ami, et une insolation n’est pas rare. Pensez à prendre un chapeau à larges bords ou procurez-vous un couvre chef sur place. De même pensez à bien vous hydrater tout au long de la journée. </p>

<p>-    Eau : attention, règle d’or pour les voyageurs au Mexique, ne JAMAIS boire l’eau du robinet. Si elle est parfaite pour prendre une douche ou un bain, cette eau reste impropre à la consommation. Préférez-lui des eaux en bouteille ou en « garrafon », sorte de très grosse bouteille présente un peu partout dans les hôtels. </p>


<p><strong>Sécurité</strong></p>
<p>Si les dernières nouvelles de la guerre des drogues ne sont guère rassurantes, ce conflit se joue presque exclusivement entre gangs, ou entre ces derniers et les forces de sécurité mexicaines. Les touristes font très rarement partie des victimes, sauf accident malencontreux et il existe de nombreuses régions parfaitement sûres au Mexique. De plus, la grande majorité des actes violents se produisent dans un nombre de lieux limité. En somme, vous ne risquez pas plus de mauvaise rencontre au Mexique qu’à Paris. 
   <p> Vol : comme dans tous les lieux touristiques, il convient de surveiller étroitement ses effets. Vous n’êtes jamais à l’abri de pickpockets ou d’autres voleurs à la petite semaine. </p>
   <p> Se balader seul : le Mexique est un pays où il fait bon se promener. Toutefois, éviter de sortir seul(e) dans des endroits peu fréquentés, de passer par des axes routiers trop secondaires et globalement de voyager de nuit. N’oubliez pas non plus que vous êtes dans un pays latin, dans lequel le machisme, même s’il reste à relativiser, tient une place prépondérante. C’est pourquoi mesdemoiselles et mesdames, il ne faut pas se formaliser si vous êtes sifflée dans la rue. Si on vous accoste, décliner poliment et froidement. </p>
